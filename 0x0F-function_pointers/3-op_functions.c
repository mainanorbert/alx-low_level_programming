#include "3-calc.h"

/**
 * op_add - returns sum
 * @a: integer1
 * @b: integer2
 * Return: returns integer
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns difference
 * @a: integer1
 * @b: integer2
 * Return: returns integer
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns product
 * @a: integer1
 * @b: integer2
 * Return: returns integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns quotient
 * @a: integer1
 * @b: integer2
 * Return: returns integer
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns remainder
 * @a: integer1
 * @b: integer2
 * Return: returns integer
 */
int op_mod(int a, int b)
{
	return (a % b);
}
